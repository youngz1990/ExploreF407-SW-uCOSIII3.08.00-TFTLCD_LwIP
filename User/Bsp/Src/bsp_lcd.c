/*
*********************************************************************************************************
*	函    数: BSP_LCDPrintString
*	说    明: LCD屏显示字符串
*	形    参: range     LCD屏上显示字符串的区域
            space     字间距行间距
            *string   要显示的字符串
            size      显示字体大小
            color     显示字体颜色
*	返    回: 错误码
*********************************************************************************************************
*/
BSP_ErrorTypedef BSP_LCDPrintString(BSP_LCDRectTypedef range, BSP_LCDSpaceTypedef space, char const *string, uint8_t size, uint16_t color)
{
  uint16_t xpos_offset = 0, ypos_offset = 0, xpos = 0, ypos = 0;
  uint16_t temp = 0;
  uint16_t line_num = 0;

  switch (size)
  {
    case BSP_LCD_FONT_HEIGHT_12:
      xpos_offset = BSP_LCD_FONT_WIDTH_12;
      ypos_offset = BSP_LCD_FONT_HEIGHT_12;
      break;
    
    case BSP_LCD_FONT_HEIGHT_16:
      xpos_offset = BSP_LCD_FONT_WIDTH_16;
      ypos_offset = BSP_LCD_FONT_HEIGHT_16;
      break;
    
    case BSP_LCD_FONT_HEIGHT_24:
      xpos_offset = BSP_LCD_FONT_WIDTH_24;
      ypos_offset = BSP_LCD_FONT_HEIGHT_24;
      break;

    default:
      return BSP_LCD_ERROR_FONT;
  }

  if(BSP_LCDDevInfo.LCD_DIR == LCD_DIR_VERTICAL)
  {
    temp = xpos_offset;
    xpos_offset = ypos_offset;
    ypos_offset = temp;
    xpos = range.LCD_XposStart;
    ypos = range.LCD_YposStart - ypos_offset / 2;
  }
  else
  {
    xpos = range.LCD_XposStart + space.LCD_WordSpace;
    ypos = range.LCD_YposStart;
  }

  while (*string != '\0')
  {
    if(BSP_LCDDevInfo.LCD_DIR == LCD_DIR_CROSSWISE)
    {
      /* 范围判断，要显示的字符在指定的范围内 */
      if((xpos + xpos_offset) > range.LCD_XposStop)      //超出行显示范围
      {
        line_num++;
        ypos = range.LCD_YposStart + line_num * ypos_offset + space.LCD_LineSpace; //换行
        if((ypos + ypos_offset) > range.LCD_YposStop)    //超出列显示范围
        {
          return BSP_LCD_ERROR_RANGE;
        }
        else
        {
          xpos = range.LCD_XposStart + space.LCD_WordSpace;
          LCD_ShowChar(xpos, ypos, *string, size, color);
        }
      }
      else
      {
        if((ypos + ypos_offset) > range.LCD_YposStop)   //超出列显示范围
        {
          return BSP_LCD_ERROR_RANGE;
        }
        else
        {
          LCD_ShowChar(xpos, ypos, *string, size, color);
        }
      }
      xpos += (xpos_offset + space.LCD_WordSpace);
    }
    else
    {
      /* 范围判断，要显示的字符在指定的范围内 */
      if(ypos < (range.LCD_YposStop + ypos_offset + space.LCD_WordSpace))      //超出行显示范围
      {
        line_num++;
        xpos = range.LCD_XposStart + line_num * xpos_offset + space.LCD_LineSpace; //换行
        if((xpos + xpos_offset) > range.LCD_XposStop)    //超出列显示范围
        {
          return BSP_LCD_ERROR_RANGE;
        }
        else
        {
          ypos = range.LCD_YposStart - space.LCD_WordSpace;
          LCD_ShowChar(xpos, ypos, *string, size, color);
        }
      }
      else
      {
        if((xpos + xpos_offset) > range.LCD_XposStop)   //超出列显示范围
        {
          return BSP_LCD_ERROR_RANGE;
        }
        else
        {
          LCD_ShowChar(xpos, ypos, *string, size, color);
        }
      }
      ypos -= (ypos_offset + space.LCD_WordSpace);
    }
    string++;
  }

  /* 记录当前坐标 */
  if(BSP_LCDDevInfo.LCD_DIR == LCD_DIR_CROSSWISE)
  {
    BSP_LCDCursor.Xpos = xpos + xpos_offset + space.LCD_WordSpace;
    BSP_LCDCursor.Ypos = ypos;
  }
  else
  {
    BSP_LCDCursor.Ypos = ypos - ypos_offset - space.LCD_WordSpace;
    BSP_LCDCursor.Xpos = xpos;
  }
  return BSP_ERROR_NONE;
}

/*
*********************************************************************************************************
*	函    数: BSP_LCDRectFillPixel
*	说    明: LCD向指定区域填充颜色
*	形    参: range     LCD屏上指定区域
            color     填充颜色
*	返    回: 无
*********************************************************************************************************
*/
void BSP_LCDRectFillPixel(BSP_LCDRectTypedef rect, uint16_t color)
{
  // uint16_t line_pixel = rect.LCD_YposStart, row_pixel = rect.LCD_XposStop - rect.LCD_XposStart;
  uint16_t line_pixel, row_pixel, x_start, y_start;
  uint16_t temp1, temp2;

  x_start = rect.LCD_XposStart > rect.LCD_XposStop ? rect.LCD_XposStop : rect.LCD_XposStart;
  y_start = rect.LCD_YposStart > rect.LCD_YposStop ? rect.LCD_YposStop : rect.LCD_YposStart;
  row_pixel = rect.LCD_XposStart > rect.LCD_XposStop ? (rect.LCD_XposStart - rect.LCD_XposStop) : (rect.LCD_XposStop - rect.LCD_XposStart);
  line_pixel = rect.LCD_YposStart > rect.LCD_YposStop ? (rect.LCD_YposStart - rect.LCD_YposStop) : (rect.LCD_YposStop-rect.LCD_YposStart);

  for(temp1 = 0; temp1 < line_pixel; temp1++)
  {
    LCD_SetCursor(x_start, y_start);
    BSP_LCD_WR_CMD(BSP_LCDDevInfo.LCD_CMD_WriteRAM);
    for(temp2 = 0; temp2 < row_pixel; temp2++)
    {
      BSP_LCD_WR_DATA(color);
    }
    y_start++;
  }
}

