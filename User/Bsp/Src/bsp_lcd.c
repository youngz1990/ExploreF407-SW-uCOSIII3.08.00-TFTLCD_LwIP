/*
*********************************************************************************************************
*	函    数: BSP_LCDPrintCell
*	说    明: 打印单元格内的内容
*	形    参: *p_cell           指向单元格数据结构的指针
*	返    回: 错误信息
*********************************************************************************************************
*/
BSP_ErrorTypedef BSP_LCDPrintCell(BSP_LCDTableCellTypedef *p_cell)
{
  BSP_LCDCellFormatTypedef format = p_cell->Format;
  uint32_t uinteger = 0;
  float floating = 0;

  Space.LCD_LineSpace = 0;
  Space.LCD_WordSpace = BSP_LCD_FONT_HEIGHT_16 / 6;

  switch (format)
  {
  case STRING:
    BSP_LCDPrintString(p_cell->CellArea, Space, (char const *)(p_cell->Content), BSP_LCD_FONT_HEIGHT_16, BLACK);
    break;

  case UINT8:
    uinteger = *((uint8_t *)(p_cell->Content));
    _uitoa(uinteger, String, 10);
    BSP_LCDPrintString(p_cell->CellArea, Space, String, BSP_LCD_FONT_HEIGHT_16, BLACK);
    break;

  case UINT16:
    uinteger = *((uint16_t *)(p_cell->Content));
    _uitoa(uinteger, String, 10);
    BSP_LCDPrintString(p_cell->CellArea, Space, String, BSP_LCD_FONT_HEIGHT_16, BLACK);
    break;

  case UINT32:
    uinteger = *((uint32_t *)(p_cell->Content));
    _uitoa(uinteger, String, 10);
    BSP_LCDPrintString(p_cell->CellArea, Space, String, BSP_LCD_FONT_HEIGHT_16, BLACK);
    break;

  case FLOAT:
    floating = *((float *)(p_cell->Content));
    _ftoa(floating, String, 2);
    BSP_LCDPrintString(p_cell->CellArea, Space, String, BSP_LCD_FONT_HEIGHT_16, BLACK);
    break;

  default:
    return BSP_LCD_ERROR_FORMAT;
    break;
  }
  return BSP_ERROR_NONE;
}
